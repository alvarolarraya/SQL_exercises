REM EJER25
SELECT P.APELLIDO1||' '||P.APELLIDO2||', '||P.NOMBRE_PILA, COUNT(DISTINCT A.SIGLAS)
FROM ASIGNATURAS A, PROFESORES P,DOCENCIA D
WHERE A.SIGLAS=D.SIGLAS AND D.PROFESOR=P.CODIGO
GROUP BY P.APELLIDO1,P.APELLIDO2,P.NOMBRE_PILA
ORDER BY P.APELLIDO1

REM EJER 26
SELECT P.APELLIDO1||' '||P.APELLIDO2||', '||P.NOMBRE_PILA
FROM ASIGNATURAS A, PROFESORES P,DOCENCIA D
WHERE A.SIGLAS=D.SIGLAS AND D.PROFESOR=P.CODIGO
GROUP BY P.APELLIDO1,P.APELLIDO2,P.NOMBRE_PILA
HAVING COUNT(DISTINCT A.SIGLAS)>1
ORDER BY P.APELLIDO1

