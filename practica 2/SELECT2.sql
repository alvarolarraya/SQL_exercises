SELECT L.NOMBRE, L.EDIFICIO, L.CAPACIDAD-AVG(A.ALUMNOS),A.NOMBRE,COUNT(DISTINCT L.CODIGO)
FROM LOCALES L, ASIGNATURAS A,DOCENCIA D
WHERE A.SIGLAS=D.SIGLAS AND D.LOCAL=L.CODIGO
GROUP BY L.NOMBRE,L.EDIFICIO,A.NOMBRE,L.CAPACIDAD
